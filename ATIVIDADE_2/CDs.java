package Entidades;

public class CDs { 
	
	private boolean arranhado;
	private boolean duplo;
	
	
	public boolean isArranhado() {
		return arranhado;
	}
	public void setArranhado(boolean arranhado) {
		this.arranhado = arranhado;
	}
	public boolean isDuplo() {
		return duplo;
	}
	public void setDuplo(boolean duplo) {
		this.duplo = duplo;
	}
	

}
